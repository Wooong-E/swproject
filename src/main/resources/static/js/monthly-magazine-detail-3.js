const langButtons = document.querySelectorAll('.lang-button');
const currentLang = localStorage.getItem('selectedLang') || 'en';

function applyTranslations(lang) {
    const currentTranslations = translations[lang];
    if (!currentTranslations) return;

    for (const key in currentTranslations) {
        const element = document.getElementById(key);
        if (element) {
            element.innerHTML = currentTranslations[key].replace(/\n/g, '<br/>');
        }
    }
}

function updateActiveButton(selectedButtonId) {
    langButtons.forEach(button => {
        button.classList.remove('active');
    });
    document.getElementById(selectedButtonId).classList.add('active');
}

langButtons.forEach(button => {
    button.addEventListener('click', function() {
        const lang = this.id.replace('lang-', '');
        localStorage.setItem('selectedLang', lang);
        applyTranslations(lang);
        updateActiveButton(this.id);
    });
});

applyTranslations(currentLang);
updateActiveButton(`lang-${currentLang}`);
